function combineTriples(mode) {
  const { starts, middles, ends } = openTemplates[mode];
  const out = [];
  for (const a of starts) for (const b of middles) for (const c of ends) out.push(`${a} ${b} ${c}`);
  return out;
}

function buildQuestions(perMode = 500) {
  const list = [];
  let id = 1;

  MODES.forEach((mode) => {
    const textPool = combineTriples(mode);
    for (let i = 0; i < perMode; i++) {
      const t = i % 4;
      const q = { id: `${mode}-${id++}`, mode, text: textPool[i % textPool.length] };
      if (t === 0) q.type = 'open';
      if (t === 1) {
        q.type = 'choice';
        q.text = `${textPool[i % textPool.length]} (Выберите вариант)`;
        q.options = choices[i % choices.length];
      }
      if (t === 2) {
        q.type = 'scale';
        q.text = scalePrompts[i % scalePrompts.length];
        q.scale = { min: 1, max: 10 };
      }
      if (t === 3) {
        q.type = 'guess';
        q.text = `Угадай ответ партнёра: ${textPool[i % textPool.length]}`;
      }
      list.push(q);
    }
  });

  return list;
}

export const QUESTIONS = buildQuestions(500); // 2000 total
